package basicSyntax

/*
	Comments are like helping texts in your Go program and they are ignored by the compiler.
	They are used to explain and clarify the code for anyone reading it, including your future self.
	There are two types of comments in Go:
	1. Single-line Comments: These comments start with // and continue until the end of the line.
	They are typically used for brief explanations or notes about specific lines of code.
	Example:
		// This is a single-line comment
	2. Multi-line Comments: These comments start with /* and end with */
/*
	They can span multiple lines and are useful for longer explanations or documentation.
	Example:
		/*
		This is a multi-line comment.
		It can span multiple lines.
		*
*/
/*Comments are essential for maintaining code readability and
helping others (or yourself) understand the purpose and functionality of the code.

*/
