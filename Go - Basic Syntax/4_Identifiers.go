package basicSyntax

/*
	Identifiers in Go
	An identifier is a name given to a variable, function, or any other user-defined item in a Go program.
	Identifiers in Go must follow certain rules:
	1. They must start with a letter (a-z, A-Z) or an underscore (_).
	2. They can be followed by letters, digits (0-9), or underscores.
	3. They are case-sensitive.
	4. They cannot be reserved keywords in Go.
	Examples of valid identifiers:
		varName
		_underscore
		var123
		identifierName
*/
